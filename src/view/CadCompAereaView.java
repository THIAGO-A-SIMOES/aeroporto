    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        
        if (txtCodigo.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Informe o CÃ³digo da Compania AÃ©rea!");
            return;
        }
        
        if (txtCompAerea.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Informe o Nome da Compania AÃ©rea!");
            return;
        }
        
        int status;
        
        CompAereaModel compAerea = new CompAereaModel();
        
        compAerea.setCodCompAerea(Integer.parseInt(txtCodigo.getText()));
        compAerea.setCompAerea(txtCompAerea.getText());
        
        if (chkSituacao.isSelected()) {
            status = 1;
        } else {
            status = 0;
        }
        
        compAerea.setSituacao(status);
        
        CompAereaController controle = new CompAereaController();
        
        if(controle.cadastraCompAerea(compAerea)){
            JOptionPane.showMessageDialog(null, "Compania AÃ©rea cadastrado!");
            alteraBotoes(true,false,false,false);
            txtCodigo.setText("");
            txtCompAerea.setText("");
            chkSituacao.setSelected(false);
            txtCodigo.setEnabled(true);
            txtCompAerea.setEnabled(false);
            chkSituacao.setEnabled(false);
        }else{
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar!");
        } 
    }//GEN-LAST:event_btnSalvarActionPerformed

