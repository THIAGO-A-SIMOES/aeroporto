    private void btnNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNovoActionPerformed
        try{
            alteraBotoes(false,true,true,true);
            btnProcurar.setEnabled(false);
            txtCodigo.setText("");
            txtCompAerea.setText("");
            chkSituacao.setSelected(true);
            txtCodigo.setEnabled(true);
            txtCompAerea.setEnabled(true);
            chkSituacao.setEnabled(true);

            CompAereaController objCompAerea = new CompAereaController();
            ResultSet rsCompAerea = objCompAerea.buscaUltimaCompAerea();

            if (rsCompAerea.next()){
                txtCodigo.setText(rsCompAerea.getString("lastCod"));
                txtCodigo.setEnabled(false);
                txtCompAerea.setFocusable(true);
            }else{

            }
        }catch(SQLException erro){
            JOptionPane.showMessageDialog(null, "CadCompAereaView: " + erro);
        }
        
    }//GEN-LAST:event_btnNovoActionPerformed

