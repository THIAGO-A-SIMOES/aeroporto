    private void txtCodigoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtCodigoFocusLost
        
        CompAereaController controle = new CompAereaController();

        if (!txtCodigo.getText().isEmpty()){
            try {
                ResultSet rs = controle.buscaCompAerea(txtCodigo.getText());
                if(rs.next()){
                    txtCodigo.setText(rs.getString("idCompAerea"));
                    if (rs.getString("situacao") == "1"){
                        chkSituacao.setSelected(true);
                    }else{
                        chkSituacao.setSelected(false);
                    }
                    
                    txtCompAerea.setText(rs.getString("descricao"));
                    alteraBotoes(false,true,true,true);
                    txtCodigo.setEnabled(true);
                    txtCompAerea.setEnabled(true);
                    chkSituacao.setEnabled(true);
                }else{
                    JOptionPane.showMessageDialog(this, "Compania Aeronave nÃ£o encontrado!");
                    txtCodigo.setText("");
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, "Erro ao pesquisar a compania aÃ©rea!");
                txtCodigo.setText("");
            }
        }else{
            return;
        }
    }//GEN-LAST:event_txtCodigoFocusLost

